                <p class="board-text"> Gail Seymour Halvorsen was born on October 10, 1920 in Salt Lake City, Utah. His parents, Basil and Luella Halvorsen, raised Gail and his three siblings on a small farm in Rigby, Idaho and then later moved to Garland, Utah where his family farmed sugar beets. Halvorsen recalls growing up watching planes soar over his Utah home and dreaming of becoming a pilot. He attended Bear River High School in Garland and graduated in 1939.</p>
				  <p>Halvorsen decided to pursue his childhood dream of becoming a pilot and in September 1941 he earned his private pilot license through the non-college Civilian Pilot Training Program and joined the newly formed Civil Air Patrol. In May 1942, Halvorsen joined the United States Army Air Corps and then attended Utah State University for two quarters while waiting for his active duty call. There he met Alta Jolley, his future wife.</p>
