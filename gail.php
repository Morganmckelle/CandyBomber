			<p class="board-text"><i>"Service before self is the only way to fulfillment in life." -Gail Halvorsen</i></p>
		  
          <p >Serving others was a priority to Halvorsen and throughout his life he remained active in humanitarian work. He frequently voluntarily represented the U.S. Air Force in re-enacting the Berlin Airlift candy drops and in 1994 Halvorsen convinced the Air Force to allow him to drop candy over Bosnia-Herzegovina as part of Operation Provide Promise. From 1986-87 he served an LDS mission to England with his wife Alta, and from 1995-97 they served another LDS mission to St. Petersburg, Russia. Halvorsen has also served in numerous volunteer positions for the LDS church including Stake President, Bishop, High Councilman and various other callings.
