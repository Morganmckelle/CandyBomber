            <div class="col-md-8 board-members-text" style="float: right">
                <p class="board-text">In March 1943, the active duty call came and Halvorsen was sent to Wichita Falls, Texas for basic training. After completing basic training he traveled to Miami, Oklahoma to train with the Royal Air Force pilots at the No. 3 British Flying Training School. Halvorsen received his wings with the Royal Air Force and was then transferred back to the Army Air Corps. He served in transport operations in the south Atlantic theater during World War II. Halvorsen’s first foreign deployment was to Natal, Brazil where he flew transport airplanes delivering supplies up and down South America, South Africa, and Ascension Island.</p>

          <p>On July 10, 1948, Halvorsen received orders to transfer to Germany to perform transport operations for the Berlin Airlift (also called “Operation Vittles”). Germany and its capital city, Berlin, had been divided between the Western Allies and the Soviet Union when World War II had ended three years earlier. When Stalin blocked ground transportation into Berlin, the United States and Great Britain used transport aircraft to drop food and other basic supplies to prevent the city from starving. Halvorsen describes the Berlin Airlift as being “a healing balm on the wounds of war.” The initial suspicions of the West Germans toward the Americans and British were slowly replaced with gratitude as the Berlin Airlift became a lifesaving operation.</p>
          </div>
        <div class=" aboutsectionbody">
          <p>While stationed in Germany, Halvorsen spent much of his free time filming the countryside on his personal handheld movie camera. While filming at Tempelhof, the main landing site for the airlift, Halvorsen noticed a group of roughly thirty children crowded behind one of the barbed-wire fences. Halvorsen recalls, "I met about thirty children at the barbed wire fence that protected Tempelhof's huge area. They were excited and told me that 'when the weather gets so bad that you can't land, don't worry about us. We can get by on a little food, but if we lose our freedom, we may never get it back.'"</p>

          <p>Halvorsen was touched by their maturity and value of freedom. He pulled two sticks of Wrigley Double-Mint gum from his pocket and gave it to the children. They broke it into tiny pieces and shared it-those who did not get any sniffed the wrappers. Halvorsen wished he had more to give and told the children the following day he would bring enough gum for all of them. He said he would drop the gum from his plane and told the children they would know it was his plane because he would wiggle his wings, a trick he had performed for his parents in summer of 1941 when he flew over his family's farm.
          </p>

          <p>Halvorsen used his ration card to buy all of the candy, chocolate and gum he could and then convinced his copilot and engineer to also give their rations. Prepared with a "big, double handful" of goodies, Halvorsen became concerned the children could be injured by the candy falling at 110 miles per hour, so he made three parachutes out of handkerchiefs and tied them to the candy to slow its descent. The next day when Halvorsen took off on his supply run he could see the group of children eagerly waiting. The engineer pushed the candy parachutes out and successfully dropped them to the kids. The children shared the treats and happily grinned as they waved to all the aircraft. Halvorsen and his crew were touched by the children's happiness and decided to include the candy parachutes in their regular supply drops. They made the candy drops once a week for the following three weeks and each time the crowd of children grew larger. </p>    

          <p>Halvorsen was called in to his airlift commander, Lieutenant General William H. Tunner, and he feared he might be court-martialed. However, Tunner had a different idea and instead expanded the operation, calling it "Operation Little Vittles." The operation officially began on September 22, 1948 and support for the effort expanded quickly. As word of Operation Little Vittles reached the United States, citizens and candy makers began donating candy. The response was so immense that Halvorsen could no longer keep up with the amount of candy and handkerchiefs being donated. Mary C. Connors offered to take over the preparation and collaborated with the National Confectioner's Association to prepare the candy and tie the handkerchief parachutes. Many other pilots joined the effort and began to drop candy every other day to the German children. The Little Vittles pilots, became known as the Rosinenbomber (Raisin Bombers) while Halvorsen was given many nicknames by the German children including "Uncle Wiggly Wings", "The Chocolate Flier", "The Gum Drop Kid" and "The Chocolate Uncle." Halvorsen estimates the operation dropped 250,000 parachutes with over 20 tons of candy to the German children. Operation Little Vittles lasted from September 22, 1948 to May 13, 1949 and brought smiles of joy to countless children's faces.</p>
